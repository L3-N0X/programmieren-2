package thd.game.level;

/**
 * This enum represents the road conditions of a level in the game.
 */
public enum RoadCondition {
    /**
     * Represents a dry road condition.
     */
    DRY,
    /**
     * Represents a wet road condition, which may affect vehicle handling.
     */
    WET,
    /**
     * Represents a snowy road condition, which significantly affects vehicle handling.
     */
    ICY;
}
